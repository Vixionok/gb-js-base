'use strict'

/**
 * [Сложение 2х чисел]
 * @param  {num} a Переменная 1
 * @param  {num} b Переменная 2
 * @return {num}   Результат
 */
function addition(a, b){
  return a+b;
}
/**
 * [Разность 2х чисел]
 * @param  {num} a Переменная 1
 * @param  {num} b Переменная 2
 * @return {num}   Результат
 */
function subtraction(a, b){
  return a-b;
}
/**
 * [Произведение 2х чисел]
 * @param  {num} a Переменная 1
 * @param  {num} b Переменная 2
 * @return {num}   Результат
 */
function multiplication(a, b){
  return a*b;
}
/**
 * [Частное 2х чисел]
 * @param  {num} a Переменная 1
 * @param  {num} b Переменная 2
 * @return {num}   Результат
 */
function division(a, b){
  return a/b;
}